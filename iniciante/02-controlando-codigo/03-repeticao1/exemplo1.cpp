#include<iostream> // cin e cout

using namespace std;

int main(){
	int n = 1; // declaro a variável n começãndo com o valor 1;

	while( n <= 10){ // enquanto n for menor ou igual a 10
		cout << n <<" "; // imprima o valor de n e um espaço
		n = n + 1; // adicione 1 ao seu valor
	}
	
	cout << "\n";

	return 0;
}
